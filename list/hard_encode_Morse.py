assert encode_morse("did you got my mail ?") == "-.. .. -..   -.-- --- ..-   --. --- -   -- -.--   -- .- .. .-..   ..--.."
assert encode_morse("TWO THInGS TO KNOW : i FORGeT THeM :C") == "- .-- ---   - .... .. -. --. ...   - ---   -.- -. --- .--   ---...   ..   ..-. --- .-. --. . -   - .... . --   ---... -.-."

